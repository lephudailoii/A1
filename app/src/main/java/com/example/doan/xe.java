package com.example.doan;

public class xe {
    private int hinhxe;
    private String tenxe;
    private  int giaxe;

    public xe(int hinhxe, String tenxe, int giaxe) {
        this.hinhxe = hinhxe;
        this.tenxe = tenxe;
        this.giaxe = giaxe;
    }



    public int getHinhxe() {
        return hinhxe;
    }

    public void setHinhxe(int hinhxe) {
        this.hinhxe = hinhxe;
    }

    public String getTenxe() {
        return tenxe;
    }

    public void setTenxe(String tenxe) {
        this.tenxe = tenxe;
    }

    public int getGiaxe() {
        return giaxe;
    }

    public void setGiaxe(int giaxe) {
        this.giaxe = giaxe;
    }


}
