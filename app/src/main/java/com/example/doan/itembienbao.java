package com.example.doan;

public class itembienbao {
    private  int hinhbb;
    private  String dongtren;
    private  String dongduoi;

    public itembienbao(int hinhbb, String dongtren, String dongduoi) {
        this.hinhbb = hinhbb;
        this.dongtren = dongtren;
        this.dongduoi = dongduoi;
    }
    public int getHinhbb() {
        return hinhbb;
    }

    public void setHinhbb(int hinhbb) {
        this.hinhbb = hinhbb;
    }

    public String getDongtren() {
        return dongtren;
    }

    public void setDongtren(String dongtren) {
        this.dongtren = dongtren;
    }

    public String getDongduoi() {
        return dongduoi;
    }

    public void setDongduoi(String dongduoi) {
        this.dongduoi = dongduoi;
    }














}
